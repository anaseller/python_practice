"""Большинство типов данный в Python можно привести
к логическому типу.
"""

# int
print(' ============= int =============')
print(bool(5))
print(bool(0))

# float
print(' ============= float =============')
print(bool(5.0))
print(bool(0.0))

# str
print(' ============= str =============')
print(bool('5'))
print(bool('0'))
print(bool(' '))
print(bool(''))
